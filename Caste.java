class Caste
{
   public static void main(String[] args)
   {
     int i = 81;
     char b = (char)i;
     double d = 323.142f;
     int c = (int)d;
     float f = 72.38f;
     int e = (int)f;
     double d1 = 323.142f;
     byte l = (byte)d1;
     System.out.println("i =" + i + " and b = " + b);
     System.out.println("d =" + d + " and c = " + c);
     System.out.println("f =" + f + " and e = " + e);
     System.out.println("d1 =" + d1 + " and l = " + l);
    }
}